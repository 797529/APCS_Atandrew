public class ThirdLoop
{
public ThirdLoop()
    {
    }
public static void main(int l){
        for(int a = 0; a < l; a++){
            for(int b = 0; b < l; b++){
                System.out.print(l + " ");
            }
            System.out.println();
        }
    }
}
